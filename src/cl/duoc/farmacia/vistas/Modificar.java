    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
        // TODO add your handling code here:
        MedicamentoControl control = new MedicamentoControl();
        String varNombre = txt_nombre.getText();
        String varLaboratorio = txt_lab.getText();
        String varTipo = txt_tipo.getText();
        int varPrecio;
        try {
            System.out.println("Inicio accion boton");
            varPrecio = Integer.parseInt(txt_precio.getText());
            control.actualizar(varNombre, varLaboratorio, varTipo, varPrecio, med.getId());
        } catch (Exception e) {
            varPrecio = 0;
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_crearActionPerformed


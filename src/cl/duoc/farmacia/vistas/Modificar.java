    private void btn_busquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_busquedaActionPerformed
        // TODO add your handling code here:
        MedicamentoControl control = new MedicamentoControl();
        med = control.buscarPorNombre(txt_busqueda.getText());
        if (med != null) {
            txt_lab.setEnabled(true);
            txt_nombre.setEnabled(true);
            txt_precio.setEnabled(true);
            txt_tipo.setEnabled(true);
            txt_lab.setText(med.getLaboratorio());
            txt_nombre.setText(med.getNombre());
            txt_tipo.setText(med.getTipo());
            txt_precio.setText(Integer.toString(med.getPrecio()));
        }
    }//GEN-LAST:event_btn_busquedaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        med = null;
        txt_lab.setEnabled(false);
        txt_nombre.setEnabled(false);
        txt_precio.setEnabled(false);
        txt_tipo.setEnabled(false);
        txt_lab.setText("");
        txt_nombre.setText("");
        txt_precio.setText("");
        txt_tipo.setText("");
        txt_busqueda.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

